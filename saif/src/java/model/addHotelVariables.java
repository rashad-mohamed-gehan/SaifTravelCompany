package model;

public class addHotelVariables 
{
    private String hotelName;
    private String hotelAddress;
    private String hotelStars;
    private int hotelCity;
    private String hotelDescription;
    private String hotelImage;
    
    public String get_hotelName(){return hotelName;}
    public void set_hotelName(String hotelName){this.hotelName=hotelName;}
    
    public String get_hotelAddress(){return hotelAddress;}
    public void set_hotelAddress(String hotelAddress){this.hotelAddress=hotelAddress;}
    
    public String get_hotelStars(){return hotelStars;}
    public void set_hotelStars(String hotelStars){this.hotelStars=hotelStars;}
    
    public int get_hotelCity(){return hotelCity;}
    public void set_hotelCity(int hotelCity){this.hotelCity=hotelCity;}
    
    public String get_hotelDescription(){return hotelDescription;}
    public void set_hotelDescription(String hotelDescription){this.hotelDescription=hotelDescription;}
    
    public String get_hotelImage(){return hotelImage;}
    public void set_hotelImage(String hotelImage){this.hotelImage=hotelImage;}    
}
