
package model;

public class addRoomVariables {
    private String hotelName;
    private String roomNumber;
    private String roomPosition;
    private String roomCapacity;
    private String roomPrice;

    public String getHotelName() {return hotelName;}
    public void setHotelName(String hotelName){this.hotelName = hotelName;}

    public String getRoomNumber() {return roomNumber;}
    public void setRoomNumber(String roomNumber) {this.roomNumber = roomNumber;}

    public String getRoomPosition() {return roomPosition;}
    public void setRoomPosition(String roomPosition) {this.roomPosition = roomPosition;}

    public String getRoomCapacity() {return roomCapacity;}
    public void setRoomCapacity(String roomCapacity) {this.roomCapacity = roomCapacity;}

    public String getRoomPrice(){return roomPrice;}
    public void setRoomPrice(String roomDescription) {this.roomPrice = roomDescription;}
}
